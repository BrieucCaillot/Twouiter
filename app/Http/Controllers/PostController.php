     * @param  \Illuminate\Http\Request $request
     * @return \Illuminate\Http\Response
     */
    public function store(Post $post, Request $request)
    {
        $request->validate([
            'message' => 'required'
        ]);

        $post->content = $request->message;
        $post->user_id = Auth::id();
        $post->save();
